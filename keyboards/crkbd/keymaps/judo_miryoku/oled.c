void render_mod_state(uint8_t modifiers) {
  //oled_write_P(PSTR("\nMods: "), false);
  oled_write_P(PSTR("\nSHF"), (modifiers & MOD_MASK_SHIFT));
  oled_white_space();
  oled_write_P(PSTR("\nCTL"), (modifiers & MOD_MASK_CTRL));
  oled_white_space();
  oled_write_P(PSTR("\nALT"), (modifiers & MOD_MASK_ALT));
  oled_white_space();
  oled_write_P(PSTR("\nMAC"), (modifiers & MOD_MASK_GUI));
}

